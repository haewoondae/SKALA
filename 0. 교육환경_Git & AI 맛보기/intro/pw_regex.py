import re

def validate_password():
    """
    사용자로부터 비밀번호를 입력받아 조건을 검증하고 결과를 출력.
    """
    pw = input("비밀번호를 입력하세요: ")

    # 전방 탐색으로 각 조건을 확인하고, 전체 길이는 최소 1자 이상
    pattern = (
        r'^(?=.*[a-z])'              # 소문자 1개 이상
        r'(?=.*[A-Z])'               # 대문자 1개 이상
        r'(?=.*\d)'                  # 숫자 1개 이상
        r'(?=.*[!@#$%^&*(),.?":{}|<>])'  # 특수문자 1개 이상
        r'.+$'                       # 임의의 문자(길이 제한 없이)
    )

    if re.fullmatch(pattern, pw):
        print("✅ 사용 가능한 비밀번호입니다!")
    else:
        print(
            "❌ 비밀번호가 조건을 만족하지 않습니다.\n"
            "- 소문자, 대문자, 숫자, 특수문자를 각각 1개 이상 포함해야 합니다."
        )

if __name__ == "__main__":
    validate_password()
